###############################################################################

rm(list = ls())

source("../operators/dominance.R")
source("../operators/nondominated_sort.R")

###############################################################################
# Population
###############################################################################

population <- list(
  
  list(objectives = c(1,5)),
  list(objectives = c(2,4)),
  list(objectives = c(3,3)),
  list(objectives = c(4,2)),
  list(objectives = c(5,1)),
  list(objectives = c(3,5)),
  list(objectives = c(5,5))
  
)

###############################################################################
# Execute
###############################################################################

result <- fastNonDominatedSort(population)

###############################################################################
# Print Fronts
###############################################################################

cat("\n")
cat("========================================\n")
cat("NON-DOMINATED SORTING\n")
cat("========================================\n\n")

for(k in seq_along(result$fronts))
{
  
  cat("Front", k, ": ")
  
  print(result$fronts[[k]])
  
}

cat("\n")

###############################################################################
# Print Ranks
###############################################################################

cat("Ranks\n")
cat("------------------------------\n")

for(i in seq_along(result$population))
{
  
  cat(
    
    "Individual",
    
    i,
    
    "Rank =",
    
    result$population[[i]]$rank,
    
    "\n"
    
  )
  
}

cat("\n========================================\n")
cat("END TEST\n")
cat("========================================\n")