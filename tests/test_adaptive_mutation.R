rm(list=ls())

graphics.off()

source("../operators/adaptive_mutation.R")

###############################################################################
# Artificial Population
###############################################################################

population <- list()

set.seed(123)

for(i in 1:100)
{
  
  population[[i]] <- list(
    
    position = runif(30)
    
  )
  
}

###############################################################################
# Compute
###############################################################################

diversity <-
  
  populationDiversity(
    
    population
    
  )

mutation <-
  
  adaptiveMutationRate(
    
    diversity
    
  )

###############################################################################
# Print
###############################################################################

cat("\n")

cat("=========================================\n")

cat("ADAPTIVE MUTATION TEST\n")

cat("=========================================\n\n")

cat("Population Diversity :", diversity, "\n")

cat("Mutation Probability :", mutation, "\n\n")