  rep(
    
    0,
    
    problem$numberOfVariables
    
  )

obj0 <-
  
  problem$evaluate(x0)

cat(
  
  "All zeros ->",
  
  round(obj0[1],6),
  
  round(obj0[2],6),
  
  "\n"
  
)

