cat("TESTING ZDT3 BENCHMARK\n")
cat("=============================================\n\n")

###############################################################################
# Load Benchmark
###############################################################################

source("../benchmark/zdt3.R")

problem <-
  
  createZDT3Benchmark()

###############################################################################
# Benchmark Information
###############################################################################

cat("Benchmark .............:", problem$name, "\n")
cat("Variables .............:", problem$numberOfVariables, "\n")
cat("Objectives ............:", problem$numberOfObjectives, "\n\n")

###############################################################################
# Random Solution
###############################################################################

set.seed(123)

x <-
  
  runif(
    
    problem$numberOfVariables,
    
    min = problem$lowerBounds,
    
    max = problem$upperBounds
    
  )

cat("Random Solution\n")
print(round(x, 4))

cat("\n")

###############################################################################
# Evaluate
###############################################################################

objectives <-
  
  problem$evaluate(x)

cat("Objectives\n")
cat("-----------------------------\n")

cat(
  
  "f1 =", round(objectives[1], 6),
  
  "\n"
  
)

cat(
  
  "f2 =", round(objectives[2], 6),
  
  "\n\n"
  
)

###############################################################################
# Reference Pareto Front
###############################################################################

pf <-
  
  problem$paretoFront()

cat(
  
  "Reference Front Points ....:",
  
  nrow(pf),
  
  "\n\n"
  
)

###############################################################################
# Plot
###############################################################################

plot(
  
  pf$f1,
  
  pf$f2,
  
  pch = 16,
  
  cex = 0.4,
  
  col = "blue",
  
  xlab = expression(f[1]),
  
  ylab = expression(f[2]),
  
  main = "ZDT3 Reference Pareto Front"
  
)

###############################################################################
# Evaluate Extreme Solutions
###############################################################################

cat("Extreme Solutions\n")
cat("-----------------------------\n")

###############################################################################
