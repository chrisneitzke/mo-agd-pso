###############################################################################

rm(list = ls())

graphics.off()

source("../benchmark/zdt1.R")
source("../metrics/generational_distance.R")

###############################################################################
# Reference Front
###############################################################################

problem <- createZDT1Benchmark()

reference <- problem$paretoFront()

###############################################################################
# Artificial Approximation
###############################################################################

front <- reference

front[,2] <- front[,2] + 0.05

###############################################################################
# Compute GD
###############################################################################

gd <-
  
  generationalDistance(
    
    front,
    
    reference
    
  )

cat("\n")

cat("==============================\n")

cat("Generational Distance =",gd,"\n")

cat("==============================\n\n")

###############################################################################
# Plot
###############################################################################

plot(
  
  reference,
  
  type="l",
  
  col="red",
  
  lwd=2,
  
  xlab="f1",
  
  ylab="f2",
  
  main="GD Test"
  
)

