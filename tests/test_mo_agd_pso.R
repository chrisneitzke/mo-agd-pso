###############################################################################
#
# Test MO-AGD-PSO
#
###############################################################################

rm(list = ls())

graphics.off()

###############################################################################
# Load Framework
###############################################################################

source("../config.R")

###############################################################################
# Load Benchmark
###############################################################################

source("../benchmark/zdt1.R")

problem <- createZDT1Benchmark()

###############################################################################
# Execute Algorithm
###############################################################################

set.seed(123)

result <-
  
  runMOAGDPSO(
    
    problem,
    
    populationSize = 100,
    
    generations = 200
    
  )

###############################################################################
# Reference Front
###############################################################################

reference <-
  
  problem$paretoFront()

###############################################################################
# Obtained Front
###############################################################################

pareto <-
  
  do.call(
    
    rbind,
    
    lapply(
      
      result$pareto,
      
      function(ind)
      {
        
        ind$objectives
        
      }
      
    )
    
  )

###############################################################################
# Plot
###############################################################################

plot(
  
  reference$f1,
  
  reference$f2,
  
  type = "l",
  
  lwd = 2,
  
  col = "red",
  
  xlab = "f1",
  
  ylab = "f2",
  
  main = "MO-AGD-PSO x ZDT1"
  
)

points(
  
  pareto[,1],
  
  pareto[,2],
  
  pch = 19,
  
  col = "blue"
  
)

legend(
  
  "topright",
  
  legend = c(
    
    "Reference Pareto Front",
    
    "MO-AGD-PSO"
    
  ),
  
  col = c(
    
    "red",
    
    "blue"
    
  ),
  
  lwd = c(
    
    2,
    
    NA
    
  ),
  
  pch = c(
    
    NA,
    
    19
    
  )
  
)

###############################################################################
# Information
###############################################################################

cat("\n")

cat("=============================================\n")
cat("MO-AGD-PSO TEST\n")
cat("=============================================\n\n")

cat("Population Size :", 100, "\n")
cat("Generations     :", 200, "\n")
cat("Pareto Solutions:", length(result$pareto), "\n\n")

cat("=============================================\n")
cat("END TEST\n")
cat("=============================================\n")