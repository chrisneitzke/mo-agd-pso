###############################################################################
#
# Hypervolume Test
#
###############################################################################

rm(list = ls())

graphics.off()

source("../metrics/hypervolume.R")

###############################################################################
# Example Front
###############################################################################

front <- matrix(
  
  c(
    
    0.00,1.00,
    
    0.20,0.60,
    
    0.40,0.40,
    
    0.60,0.20,
    
    0.80,0.10,
    
    1.00,0.00
    
  ),
  
  ncol=2,
  
  byrow=TRUE
  
)

###############################################################################
# Compute
###############################################################################

hv <-
  
  hypervolume2D(
    
    front,
    
    referencePoint=c(1.1,1.1)
    
  )

cat("\n")

cat("==============================\n")

cat("Hypervolume =",hv,"\n")

cat("==============================\n\n")

###############################################################################
# Plot
###############################################################################

plot(
  
  front,
  
  pch=19,
  
  xlab="f1",
  
  ylab="f2",
  
  main="Hypervolume Test"
  
)

lines(front,col="red",lwd=2)

points(
  
  1.1,
  
  1.1,
  
  pch=4,
  
  cex=2,
  
  lwd=2
  
)