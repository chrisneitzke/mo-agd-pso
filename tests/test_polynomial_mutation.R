###############################################################################
#
# Polynomial Mutation Test
#
###############################################################################

rm(list = ls())

graphics.off()

source("../operators/polynomial_mutation.R")

###############################################################################
# Parent
###############################################################################

parent <- c(
  
  0.20,
  
  0.40,
  
  0.60,
  
  0.80
  
)

lower <- rep(0,4)

upper <- rep(1,4)

###############################################################################
# Execute
###############################################################################

set.seed(123)

child <-
  
  polynomialMutation(
    
    parent,
    
    lower,
    
    upper,
    
    eta = 20,
    
    probability = 1.0
    
  )

###############################################################################
# Print
###############################################################################

cat("\n")
cat("=========================================\n")
cat("POLYNOMIAL MUTATION TEST\n")
cat("=========================================\n\n")

cat("Parent\n")
print(round(parent,4))

cat("\nChild\n")
print(round(child,4))

cat("\nDifference\n")
print(round(child-parent,6))