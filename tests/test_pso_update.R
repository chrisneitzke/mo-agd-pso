###############################################################################

rm(list = ls())

graphics.off()

source("../operators/pso_update.R")

###############################################################################
# Particle
###############################################################################

position <- c(
  
  0.20,
  
  0.50,
  
  0.80
  
)

velocity <- c(
  
  0,
  
  0,
  
  0
  
)

###############################################################################
# Personal Best
###############################################################################

pBest <- c(
  
  0.25,
  
  0.45,
  
  0.75
  
)

###############################################################################
# Leader
###############################################################################

leader <- c(
  
  0.90,
  
  0.20,
  
  0.40
  
)

###############################################################################
# Execute
###############################################################################

set.seed(123)

result <-
  
  psoUpdate(
    
    position,
    
    velocity,
    
    pBest,
    
    leader,
    
    lowerBounds = c(0,0,0),
    
    upperBounds = c(1,1,1)
    
  )

###############################################################################
# Print
###############################################################################

cat("\n")
cat("=========================================\n")
cat("PSO UPDATE TEST\n")
cat("=========================================\n\n")

cat("Old Position\n")
print(round(position,4))

cat("\nNew Position\n")
print(round(result$position,4))

cat("\nVelocity\n")
print(round(result$velocity,4))