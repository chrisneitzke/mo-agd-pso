###############################################################################
#
# PSO Update Operator
#
# MO_AGD_PSO
#
###############################################################################

psoUpdate <- function(position,
                      velocity,
                      pBestPosition,
                      leaderPosition,
                      lowerBounds,
                      upperBounds,
                      w = 0.729,
                      c1 = 1.49445,
                      c2 = 1.49445)
{
  
  nVar <- length(position)
  
  ###########################################################################
  # Initialize Velocity
  ###########################################################################
  
  if(is.null(velocity))
  {
    
    velocity <- rep(0, nVar)
    
  }
  
  ###########################################################################
  # Random Numbers
  ###########################################################################
  
  r1 <- runif(nVar)
  
  r2 <- runif(nVar)
  
  ###########################################################################
  # Velocity Update
  ###########################################################################
  
  velocity <-
    
    w * velocity +
    
    c1 * r1 * (pBestPosition - position) +
    
    c2 * r2 * (leaderPosition - position)
  
  ###########################################################################
  # Position Update
  ###########################################################################
  
  position <- position + velocity
  
  ###########################################################################
  # Bounds
  ###########################################################################
  
  position <-
    
    pmax(
      
      lowerBounds,
      
      pmin(
        
        upperBounds,
        
        position
        
      )
      
    )
  
  ###########################################################################
  # Return
  ###########################################################################
  
  return(
    
    list(
      
      position = position,
      
      velocity = velocity
      
    )
    
  )
  
}