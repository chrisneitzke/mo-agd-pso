###############################################################################
# Create Individual
###############################################################################

createIndividual <- function(problem)
{
  
  ###########################################################################
  # Random Position
  ###########################################################################
  
  position <-
    
    runif(
      
      problem$numberOfVariables,
      
      min = problem$lowerBounds,
      
      max = problem$upperBounds
      
    )
  
  ###########################################################################
  # Evaluate
  ###########################################################################
  
  objectives <-
    
    problem$evaluate(position)
  
  ###########################################################################
  # Create Individual
  ###########################################################################
  
  individual <- list(
    
    ###############################################################
    # Decision Variables
    ###############################################################
    
    position = position,
    
    ###############################################################
    # Objective Values
    ###############################################################
    
    objectives = objectives,
    
    ###############################################################
    # NSGA-II
    ###############################################################
    
    rank = NA,
    
    crowding = 0,
    
    ###############################################################
    # PSO
    ###############################################################
    
    velocity = rep(
      
      0,
      
      problem$numberOfVariables
      
    ),
    
    pBestPosition = position,
    
    pBestObjectives = objectives,
    
    ###############################################################
    # AGD
    ###############################################################
    
    mutationRate = NA,
    
    diversity = NA
    
  )
  
  return(individual)
  
}

###############################################################################
# Create Population
###############################################################################

createPopulation <- function(problem,
                             populationSize)
{
  
  population <-
    
    vector(
      
      "list",
      
      populationSize
      
    )
  
  for(i in seq_len(populationSize))
  {
    
    population[[i]] <-
      
      createIndividual(
        
        problem
        
      )
    
  }
  
  return(population)
  
}