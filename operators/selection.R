###############################################################################
#
# Binary Tournament Selection
#
# Deb et al. (2002)
#
###############################################################################

binaryTournamentSelection <- function(population)
{
  
  n <- length(population)
  
  i <- sample.int(n, 1)
  
  j <- sample.int(n, 1)
  
  while(i == j)
  {
    
    j <- sample.int(n, 1)
    
  }
  
  a <- population[[i]]
  
  b <- population[[j]]
  
  ###########################################################################
  # Rank
  ###########################################################################
  
  if(a$rank < b$rank)
  {
    
    return(a)
    
  }
  
  if(b$rank < a$rank)
  {
    
    return(b)
    
  }
  
  ###########################################################################
  # Crowding
  ###########################################################################
  
  if(a$crowding > b$crowding)
  {
    
    return(a)
    
  }
  
  if(b$crowding > a$crowding)
  {
    
    return(b)
    
  }
  
  ###########################################################################
  # Tie
  ###########################################################################
  
  if(runif(1) < 0.5)
  {
    
    return(a)
    
  }
  
  return(b)
  
}